export const oneYearFromNow = () =>
  new Date(
    Date.now() + 365 * 24 * 60 * 60 * 1000 // One Year
  )


export const thirtyDaysFromNow = () =>
  new Date(
    Date.now() + 30 * 24 * 60 * 60 * 1000
  )

export const fifteenDaysFromNow = () =>
  new Date(
    Date.now() + 15 * 24 * 60 * 60 * 1000
  )
